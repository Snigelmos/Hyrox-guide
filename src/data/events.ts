export function getEventsByYear(year: number): HyroxEvent[] {
  return EVENTS.filter((e) => e.year === year).sort(
    (a, b) => a.startDate.localeCompare(b.startDate)
  );
}

export function getEventsByRegion(year: number, region: HyroxEvent["region"]): HyroxEvent[] {
  return getEventsByYear(year).filter((e) => e.region === region);
}

export function allEventPaths(): { year: number; slug: string }[] {
  return EVENTS.map((e) => ({ year: e.year, slug: e.slug }));
}

export function formatEventDate(startDate: string, endDate?: string): string {
  const s = new Date(startDate);
  const fmt: Intl.DateTimeFormatOptions = { month: "long", day: "numeric", year: "numeric" };
  if (endDate) {
    const e = new Date(endDate);
    const sameMonth = s.getUTCMonth() === e.getUTCMonth();
    if (sameMonth) {
      return `${s.toLocaleDateString("en-US", { month: "long", day: "numeric" })}–${e.getUTCDate()}, ${e.getUTCFullYear()}`;
    }
    return `${s.toLocaleDateString("en-US", { month: "long", day: "numeric" })}–${e.toLocaleDateString("en-US", fmt)}`;
  }
  return s.toLocaleDateString("en-US", fmt);
}

/**
 * Build-time guard: throws if the same `${year}/${slug}` appears more than
 * once in EVENTS, or if a live EVENTS entry collides with a RETIRED slug.
 *
 * Runs eagerly at module load (i.e. during `astro build`) so the deploy
 * fails loudly instead of silently emitting a broken `getStaticPaths`
 * collision or a redirect that overrides a real page.
 */
function assertSlugsUnique(): void {
  const seen = new Map<string, string>();
  for (const e of EVENTS) {
    const key = `${e.year}/${e.slug}`;
    if (seen.has(key)) {
      throw new Error(
        `[events.ts] Duplicate event slug for ${key} — every (year, slug) pair must be unique. Existing: ${seen.get(key)}, conflict: ${e.city}.`,
      );
    }
    seen.set(key, e.city);
  }
  for (const r of RETIRED_EVENT_SLUGS) {
    const key = `${r.year}/${r.slug}`;
    if (seen.has(key)) {
      throw new Error(
        `[events.ts] Slug ${key} is in both EVENTS and RETIRED_EVENT_SLUGS — a retired slug cannot also be a live event. Remove it from one of the two.`,
      );
    }
  }
  // Explainer slugs must not collide with active events or with redirect-only
  // retired slugs. They render an explainer page; the redirect lists must skip
  // these slugs.
  const retiredKeys = new Set(
    RETIRED_EVENT_SLUGS.map((r) => `${r.year}/${r.slug}`),
  );
  for (const r of RETIRED_EVENT_EXPLAINERS) {
    const key = `${r.year}/${r.slug}`;
    if (seen.has(key)) {
      throw new Error(
        `[events.ts] Slug ${key} is in both EVENTS and RETIRED_EVENT_EXPLAINERS — an explainer slug cannot also be a live event. Remove it from one of the two.`,
      );
    }
    if (retiredKeys.has(key)) {
      throw new Error(
        `[events.ts] Slug ${key} is in both RETIRED_EVENT_SLUGS and RETIRED_EVENT_EXPLAINERS — explainers take precedence over redirects. Remove it from one of the two.`,
      );
    }
  }
}

