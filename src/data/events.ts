/**
 * Hyrox events dataset.
 *
 * Used to generate /events/[year]/[city] pages programmatically with Event
 * JSON-LD for rich search results. The list covers the full 2026 calendar
 * (seasons 25/26 and 26/27), cross-referenced against hyrox.com and the
 * gowod / hyresult.com calendars. Adult races only — Hyrox Youngstars
 * (kids' format) is intentionally excluded.
 */

export interface HyroxEvent {
  slug: string;
  city: string;
  country: string;
  countryCode: string; // ISO
  year: number;
  startDate: string; // YYYY-MM-DD
  endDate?: string;
  venue: string;
  registrationUrl?: string;
  officialUrl: string;
  resultsUrl?: string; // link to official results (e.g. hyresult.com) once race is done
  notes?: string;
  confirmed: boolean;
  sponsor?: string;
  region?: "EU" | "NA" | "APAC" | "ME" | "SA" | "AF";
  populationDescriptor?: string;
  divisions?: string[];
  /**
   * Alternate-language city names for SEO. Surfaced in the event page title,
   * H1 subtitle, and FAQ so non-English searchers (e.g. "Hyrox Milano 2026",
   * "Hyrox München 2026") can find the page.
   */
  localNames?: string[];
}

/**
 * Returns true if the event's end date (or start date if no end) is before
 * the supplied reference date (defaults to today at build time).
 */
export function isPastEvent(event: HyroxEvent, now: Date = new Date()): boolean {
  const end = new Date(event.endDate ?? event.startDate);
  // Treat the full end day as still-current by advancing to next day midnight
  end.setDate(end.getDate() + 1);
  return now >= end;
}

