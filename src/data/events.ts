/**
 * Hyrox events dataset.
 *
 * Used to generate /events/[year]/[city] pages programmatically with Event
 * JSON-LD for rich search results. The list covers the full 2026 calendar
 * (seasons 25/26 and 26/27), cross-referenced against hyrox.com and the
 * gowod / hyresult.com calendars. Adult races only — Hyrox Youngstars
 * (kids' format) is intentionally excluded.
 */

export interface HyroxEvent {
  slug: string;
  city: string;
  country: string;
  countryCode: string; // ISO
  year: number;
  startDate: string; // YYYY-MM-DD
  endDate?: string;
  venue: string;
  registrationUrl?: string;
  officialUrl: string;
  resultsUrl?: string; // link to official results (e.g. hyresult.com) once race is done
  notes?: string;
  confirmed: boolean;
  sponsor?: string;
  region?: "EU" | "NA" | "APAC" | "ME" | "SA" | "AF";
  populationDescriptor?: string;
  divisions?: string[];
  /**
   * Alternate-language city names for SEO. Surfaced in the event page title,
   * H1 subtitle, and FAQ so non-English searchers (e.g. "Hyrox Milano 2026",
   * "Hyrox München 2026") can find the page.
   */
  localNames?: string[];
  /**
   * Verifiable street address of the venue. Surfaced on the event page so
   * spectators can drop it straight into a maps app. Only populate when
   * cross-checked against the venue's own site or hyrox.com — never guess.
   */
  venueAddress?: string;
  /**
   * Direct link to the official Hyrox venue PDF / course map. Hyrox
   * publishes these per-venue (e.g.
   * `hyrox.com/wp-content/uploads/<year>/<month>/HYROX<CITY>_VenueMap-<Month-Year>.pdf`)
   * usually a few days before race weekend. Leave undefined until a
   * specific URL has been verified — the event page falls back to the
   * official event page link plus an "in the Hyrox app" note.
   */
  courseMapUrl?: string;
  /**
   * Optional source page for a map image when the embeddable image URL is
   * hosted on a CDN rather than a stable page. Example: Facebook community
   * posts expose a signed CDN image URL but the post itself is the durable
   * source we should cite.
   */
  courseMapSourceUrl?: string;
  /**
   * Optional canonical Hyrox event-page URL (e.g.
   * `https://hyrox.com/event/hyrox-berlin/`). Used by
   * scripts/scan-venue-maps.mjs to auto-discover the published venue map:
   * the scanner fetches this page, finds the official course-map asset, and
   * writes it to `courseMapUrl`. The generic `officialUrl`
   * (`find-your-race`) is not enough because it isn't event-specific. Leave
   * undefined until the specific hyrox.com/event/<slug>/ URL is verified —
   * the scanner will report and skip events it can't resolve rather than
   * guessing.
   */
  hyroxEventUrl?: string;
  /**
   * Optional one-liner characterising the venue's running loop — "fast",
   * "neutral", or "tight" with a short note. Athletes use the venue map
   * to assess this; we surface the editorial summary so first-timers
   * can pace accordingly. Only populate when we've reviewed the loop
   * via the published venue map or past results split data.
   */
  trackProfile?: {
    speed: "fast" | "neutral" | "technical";
    note: string;
  };
  /**
   * Per-event venue notes that override the city-evergreen `cityNotes`
   * on the event page. Use this when an event runs at a venue that is
   * NOT the city's regular Hyrox host — e.g. the 2026 World Championship
   * runs at Strawberry Arena in Solna while the regular Stockholm regional
   * stop runs at Stockholmsmässan in Älvsjö. EventCourseSection.astro
   * prefers `venueNotes` over the city-evergreen `cityNotes` when present
   * so the venue copy on the page matches the venue header. The
   * /hyrox/[city]/ evergreen guide is unaffected.
   */
  venueNotes?: { title: string; detail: string }[];
}

/**
 * Returns true if the event's end date (or start date if no end) is before
 * the supplied reference date (defaults to today at build time).
 */
export function isPastEvent(event: HyroxEvent, now: Date = new Date()): boolean {
  const end = new Date(event.endDate ?? event.startDate);
  // Treat the full end day as still-current by advancing to next day midnight
  end.setDate(end.getDate() + 1);
  return now >= end;
}

